
public class Pokemon {
	private int hitPoints;
	private int attack;
	private int defense;
	private int spAttack;
	private int spDefense;
	private int speed;
	
	public Pokemon(){}
	public Pokemon(int hp, int a, int d, int spAtt, int spDef, int spd){
		hitPoints = hp;
		attack = a;
		defense = d;
		spAttack = spAtt;
		spDefense = spDef;
		speed = spd;
	}
}
